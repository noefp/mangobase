      <h4 class="p_font18">
        Currently, there are several mango genome sequences available. 
        Sequence and annotation data are available in the <a href="/easy_gdb/downloads.php">Downloads section</a>.
      </h4>
      <p>
				<ul>
					<li>cv. Irwin, by  <a href="https://doi.org/10.1002/tpg2.20441" target="_blank">Wijesundara et al. 2024</a></li>
					<li>cv. Tommy Atkins, by the <a href="https://doi.org/10.1186/s12870-021-02858-1" target="_blank">Mango Genome Consortium 2021</a></li>
					<li>cv. Alphonso, by <a href="https://doi.org/10.1186/s13059-020-01959-8" target="_blank">Wang et al. 2020</a> Available at the <a href="https://www.ncbi.nlm.nih.gov/data-hub/genome/GCF_011075055.1/" target="_blank"> NCBI</a>.</li>
					<li>cv. Irwin, by <a href="https://doi.org/10.3389/fpls.2021.749108" target="_blank">Ma et al. 2021</a> Only raw data available at the <a href="https://ngdc.cncb.ac.cn/search/?dbId=&q=CRA004336" target="_blank"> NGDC CNCB</a>.</li>
					<li>cv. Hong Xiang Ya, by <a href="https://doi.org/10.1101/2020.02.22.960880" target="_blank">Li et al. 2020</a> Available at the <a href="https://ngdc.cncb.ac.cn/gwh/Assembly/967/show" target="_blank"> NGDC CNCB Genome Warehouse</a>.</li>
