<p>
  RNA-seq of <i>Mangifera indica</i> pulp (mesocarp) fruit ripening from cv. Tainong
</p>
<h4>Experimental Conditions (3 replicates):</h4>
  <ul>
    <li>Tainong pulp of fruits harvested at 40, 60, 80, and 90 days after flowering (DAF)</li>
    <li>Tainong pulp of fruits harvested at 90 days and stored for 0, 4, 8 and 12 days after piciking (DAP)</li>
  </ul>
<p>
  Samples were collected from the nursery at different stages of fruit development (40, 60, 80, and 90 days after the flowering stage began).
  Fruit samples harvested at 90 days after the flowering were also kept for 4, 8, and 12 days in the laboratory 
  at a controlled temperature of 25Â°C and a humidity level of 95%. 
  cDNA libraries were sequenced by PE sequencing (100 x 2) with Illumina HiSeq 2500 and normalized to TPM.
  
  This dataset was published by <a href="https://doi.org/10.3389/fpls.2021.666805" target="_blank">Xin et al. 2021</a> 
  and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA697524" target="_blank">PRJNA697524</a>.
</p>
