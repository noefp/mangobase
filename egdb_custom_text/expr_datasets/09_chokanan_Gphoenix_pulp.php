<p>
  RNA-seq of <i>Mangifera indica</i> pulp from varieties Chokanan (firm), and Golden phoenix and Water lily (less-firm pulp).
</p>
<h4>Experimental Conditions (3 replicates):</h4>
  <ul>
    <li>Chokanan ripe mango.</li>
    <li>Chokanan unripe mango.</li>
    <li>Golden phoenix + Water lily ripe mango.</li>
    <li>Golden phoenix + Water lily unripe mango.</li>
  </ul>
<p>
  Mango fruit varieties (‘Chokanan’, ‘Golden phoenix’ and ‘Water lily’) were obtained from a commercial supplier at the mature green stage. 
  Following this, fruits were washed in running tap water, air dried and allowed to ripen at ambient temperature (25 ± 1 °C). 
  Fruits were sampled at the unripe and ripe stages, using three individual fruits per ripening stage.
  Equal amounts of RNA samples from the less-firm mango varieties ‘Golden phoenix’ and ‘Water lily’ were combined to represent the less-firm mango group.
  cDNA libraries were sequenced by PE sequencing (100 x 2) with Illumina Hiseq 2000 and normalized to TPM.
  
  This dataset was published by <a href="https://doi.org/10.1007/s11033-020-05519-y" target="_blank">Lawson et al. 2020</a> 
  and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA515564 " target="_blank">PRJNA515564</a>.
</p>

