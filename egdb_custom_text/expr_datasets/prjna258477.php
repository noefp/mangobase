<p>
  RNA-seq of <i>Mangifera indica</i> (cultivar Kent) pulp (mesocarp) tissue pulverized with liquid-nitrogen. 
  Sampling was done at day 1 (mature-green mango) and day 10 (ripe mango), using two individual fruits for each ripening stage.
</p>
<h4>Experimental Conditions (2 replicates):</h4>
  <ul>
    <li>Pulp unripe mango (mature-green mango).</li>
    <li>Pulp ripe mango.</li>
  </ul>
<p>
  Mango fruit cultivar “Kent” was harvested at the National Institute of Forestry, 
  Agricultural and Veterinary Research (INIFAP) research station located in Navojoa, Sonora, Mexico (27°03′49.33′′ N and 109°30′11.42′′ W). 
  Fruits were disinfected with chlorinated water and stored at 20°C up to 10 days. 
  cDNA libraries were sequenced by PE sequencing (100 x 2) with Illumina Hiseq 2000 and normalized to TPM.
  
  This dataset was published by <a href="https://doi.org/10.3389/fpls.2015.00062" target="_blank">Dautt-Castro et al. 2015</a> 
  and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA258477 " target="_blank">PRJNA258477 </a>.
</p>

