<p>
  RNA-seq of <i>Mangifera indica</i> (cultivar Shelly) peel after 0, 4, 17 and 48 hours following hot water brush (HWB) 
  post-harvest treatment and their controls (no post-harvest treatment).
</p>
<h4>Experimental Conditions (These experiments only contain 1 replicate):</h4>
  <ul>
    <li>Peel 0, 4, 17 and 48 hours controls.</li>
    <li>Peel 0, 4, 17 and 48 hours following postharvest hot water treatment.</li>
  </ul>
<p>
  Postharvest treatment by hot water brushing (HWB) for 15–20 s was introduced commercially to improve fruit quality and reduce postharvest disease. 
  This treatment enabled successful storage for 3–4 weeks at 12°C, with improved color and reduced disease development, 
  but it enhanced lenticel discoloration on the fruit peel. 
  Freshly harvested mango fruits (<i>Mangifera indica L.</i> cv. Shelly) were obtained from trees in commercial orchards in the north of Israel. 
  cDNA libraries were sequenced by SE sequencing with Illumina Hiseq 2000 and normalized to TPM.
  This dataset was published by <a href="https://doi.org/10.1186/1471-2164-15-957" target="_blank">Luria et al. 2014</a> 
  and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA227243" target="_blank">PRJNA227243</a>.
</p>
