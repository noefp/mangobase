<p>
  RNA-seq of <i>Mangifera indica</i> (cultivar Shelly) from the peel of the red and green sides of the red and green fruit inoculated with <i>C. gloeosporioides</i> at time 0, 2, 7 days post inoculation.
</p>
<h4>Experimental Conditions (3 replicates):</h4>
  <ul>
    <li>Peel from Green Fruit Green Side at 0, 2, 7 days post inoculation (dpi).</li>
    <li>Peel from Green Fruit Red Side at 0, 2, 7 dpi.</li>
    <li>Peel from Red Fruit Green Side at 0, 2, 7 dpi.</li>
    <li>Peel from Red Fruit Red Side at 0, 2, 7 dpi.</li>
  </ul>
<p>
  Mango fruits were harvested in July 2017. The fruit were picked from two different positions of the canopy in the orchard: the ‘red’ colored fruit (RF) from the exterior position with direct exposure to sunlight, and the ‘green’ colored fruit (GF) from the interior area in the shaded part of the canopy. cDNA libraries were sequenced by SE sequencing with Illumina HiSeq 2500 and normalized to TPM. This dataset was published by <a href="https://academic.oup.com/hr/article/doi/10.1038/s41438-020-00452-4/6446603" target="_blank">Sudheeran PK et al. 2021</a>. Raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA575336" target="_blank">PRJNA575336 </a>.
</p>

