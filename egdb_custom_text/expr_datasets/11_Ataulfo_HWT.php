<p>
  RNA-seq of <i>Mangifera indica</i> (cultivar Ataulfo) pulp after 1 and 10 days following hot water brush (HWB) post-harvest treatment and their controls (no post-harvest treatment). Day 1 is unripe stage and day 10 is the ripe stage.
</p>
<h4>Experimental Conditions (2 replicates):</h4>
  <ul>
    <li>Pulp 1 and 10 days controls</li>
    <li>Pulp 1 and 10 days following post-harvest hot water treatment.</li>
  </ul>
<p>
  Mature-green stage mango (Mangifera indica L.) fruit cultivar “Ataulfo” were obtained at the Diazteca packing facility in Escuinapa, Sinaloa, México, where they were subjected to the United States Agricultural Department APHIS hot water treatment (HWT). cDNA libraries were sequenced by PE sequencing (100 x 2) with Illumina Genome Analyzer IIx and normalized to TPM. This dataset was published by <a href="https://doi.org/10.1016/j.scienta.2017.09.031" target="_blank">Dautt-Castro et al. 2018</a> and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA286253" target="_blank">PRJNA286253</a>.
</p>

