<p>
  RNA-seq of <i>Mangifera indica</i> pulp and peel on unripe and ripe stages of the varieties Hongyu, Guire-82, and Sensation, 
  which show different coloration during maturation stages.
</p>
<h4>Experimental Conditions (3 replicate):</h4>
  <ul>
    <li>Unripe peel and pulp of cv. Hongyu (Hon) and Guire-82 (Gui)</li>
    <li>Ripe Peel and pulp of cv. Hongyu (Hon) and Guire-82 (Gui)</li>
    <li>Peel and pulp at 94, 100, 106 and 112 days after pollination (DAP) of cv. Sensation</li>
  </ul>
<p>
  94, 100, 106 and 112 DAP are equivalent to 2, 8, 14, 20 days not-bagged fruit.
  <br>
  <br>
  cDNA libraries were sequenced by PE sequencing (100 x 2) with Illumina Hiseq 3000 and normalized to TPM.
  
  This dataset was published by <a href="https://doi.org/10.1186/s13059-020-01959-8" target="_blank">Wang et al. 2020 </a> 
  and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA487154" target="_blank">PRJNA487154</a>.
</p>

