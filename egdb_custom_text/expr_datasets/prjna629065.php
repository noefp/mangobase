<p>
  RNA-seq of <i>Mangifera indica</i> pulp from 
  two mango cultivars with different sweetness 
  (high-sweet mango Tainong-1 and low-sweet mango Renong-1) were used to study fruit ripening.
</p>
<h4>Experimental Conditions (3 replicates):</h4>
  <ul>
    <li>Renong pulp 30 days after pollination (dap) - Renong-1 unripe</li>
    <li>Renong pulp 95 dap - Renong-1 early ripe</li>
    <li>Renong pulp 130 dap - Renong-1 partially ripe</li>
    <li>Renong pulp fully ripe</li>
    <li>Tainong pulp 30 dap - Tainong-1 unripe</li>
    <li>Tainong pulp 60 dap - Tainong-1 early ripe</li>
    <li>Tainong pulp 100 dap - Tainong-1 partially ripe</li>
    <li>Tainong pulp fully ripe</li>
  </ul>
<p>
  Fruit samples were collected at four ripening stages including unripe (~ 30 days after pollination), 
  early ripe (~ 60 days after pollination), partial ripe (~ 100 days after pollination) and 
  ripe (~ 110 days after pollination) from 3 years old plants held at the experimental station of the 
  South Subtropical Crops Research Institute of the Chinese Academy of Tropical Agricultural Sciences, Zhanjiang, China. 
  Plants were grown in natural environment and five mixed fruit samples per plant were collected from three different plants of each cultivar (three biological replicates).
  cDNA libraries were sequenced by PE sequencing (300 x 2) with Illumina HiSeq 2500 and normalized to TPM.
  
  This dataset was published by <a href="https://doi.org/10.1016/j.ygeno.2020.07.038" target="_blank">Li et al. 2020</a> 
  and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA629065" target="_blank">PRJNA629065</a>.
</p>
