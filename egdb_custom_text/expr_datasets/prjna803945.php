<p>
  RNA-seq of <i>Mangifera indica</i> pulp (mesocarp) fruit ripening from cv. Chokanan,
  at immature and ripening stages within the same season.
</p>
<h4>Experimental Conditions (2 replicates):</h4>
  <ul>
    <li>Unripe pulp 21 Days After Pollination (DAP)</li>
    <li>Ripe pulp 75 DAP</li>
  </ul>
<p>
  Fruits were harvested from two trees that were grown on the same soil and from the same rootstock, 
  thus minimizing any external factor and genetic variation that may affects the fruit development. 
  The fruits were harvested at 21 Days After Pollination (DAP) for immature stage, 
  and at 75 DAP for the ripening stage.
  The fruits were dissected by removing the skin where the mesocarp was cut into 1 cm cubes.
  Two biological replicate of mango fruits mesocarp were sequences from each stages.
  cDNA libraries were sequenced by SE sequencing with Illumina Hiseq 4000 and normalized to TPM.
  
  This dataset was published by <a href="https://doi.org/10.1016/j.dib.2022.108160" target="_blank">Karim. et al. 2022 </a> 
  and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA803945" target="_blank">PRJNA803945</a>.
</p>
<p>
  <br>
  <b>DAP</b> = Days After Pollination
</p>
